def emit_cases() -> list[dict[str, str]]:
    cases: list[dict[str, str]] = []
    idx = 1
    for category, questions in PROJECT_TOPICS.items():
        for query in questions:
            cases.append(
                {
                    "id": f"R500-{idx:03d}",
                    "scope": "project",
                    "category": category,
                    "model": PROJECT_MODEL,
                    "query": query,
                    "expected_behavior": "answer_from_project_sources",
                    "notes": "Synthetic project case for retrieval and grounded answer coverage.",
                }
            )
            idx += 1

    project_needed = 350 - len(cases)
    project_seed = list(cases)
    for i in range(project_needed):
        base = project_seed[i % len(project_seed)]
        cases.append(
            {
                "id": f"R500-{idx:03d}",
                "scope": "project",
                "category": base["category"],
                "model": PROJECT_MODEL,
                "query": base["query"].replace("Что", "Где в документации").replace("Как", "Каким образом в документации"),
                "expected_behavior": "answer_from_project_sources",
                "notes": "Synthetic project paraphrase for retrieval robustness.",
            }
        )
        idx += 1

    for query in BOUNDARY:
        cases.append(
            {
                "id": f"R500-{idx:03d}",
                "scope": "boundary",
                "category": "security_or_ambiguous_project_boundary",
                "model": BOUNDARY_MODEL,
                "query": query,
                "expected_behavior": "answer_if_grounded_or_refuse_if_abuse",
                "notes": "Boundary case: should distinguish project security lookup from harmful/non-project request.",
            }
        )
        idx += 1

    for query in OUT_OF_SCOPE:
        cases.append(
            {
                "id": f"R500-{idx:03d}",
                "scope": "out_of_scope",
                "category": "non_project",
                "model": OUT_MODEL,
                "query": query,
                "expected_behavior": "refuse_or_no_answer_without_llm_grounding",
                "notes": "Non-project control case.",
            }
        )
        idx += 1

    if len(cases) != 500:
        raise RuntimeError(f"Expected 500 cases, got {len(cases)}")
    return cases


def write_jsonl(path: Path, rows: Iterable[dict[str, str]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="\n") as fp:
        for row in rows:
            fp.write(json.dumps(row, ensure_ascii=False) + "\n")


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate realistic 500 eval dataset")
    parser.add_argument("--output", default=str(DEFAULT_OUTPUT))
    args = parser.parse_args()

    output_path = Path(args.output)
    cases = emit_cases()
    write_jsonl(output_path, cases)

    scope_counts: dict[str, int] = {}
    category_counts: dict[str, int] = {}
    for case in cases:
        scope_counts[case["scope"]] = scope_counts.get(case["scope"], 0) + 1
        category_counts[case["category"]] = category_counts.get(case["category"], 0) + 1

    print(
        json.dumps(
            {
                "status": "ok",
                "output": str(output_path),
                "total": len(cases),
                "scope_counts": scope_counts,
                "category_counts": category_counts,
            },
            ensure_ascii=False,
            indent=2,
        )
    )


if __name__ == "__main__":
    main()
